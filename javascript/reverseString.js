/**
 * Visit https://leetcode.com/problems/reverse-string/description/
 * for full problem description
 * 
 * Write a function that reverses a string. 
 * The input string is given as an array of characters s.
 * You must do this by modifying the input array in-place with O(1) extra memory.
 * 
 * @param {character[]} s
 * @return {void} Do not return anything, modify s in-place instead.
 */
var reverseString = function (s) {
    // Since this function takes an array as argument,
    // there's a built-in function to reverse the elements of an array,
    // so I'll just call that.
    s.reverse();
};